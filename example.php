    die("need to set \$_GET['hw']");

$dl->setFilter("hw", strtolower($_GET['hw']));

if ($dl->process() < 1)
{
    echo "Not Found!";
}
else
{
    $arr = $dl->GetResult();
    echo array2json($arr);
}
